#!/usr/bin/pyhon3
""" Inherited parent class """
import uuid
from datetime import datetime
from models import storage


class BaseModel:
    """ Defines all common attributes """
    def __init__(self, *args, **kwargs):
        """ initialize all the attributes """
        if not kwargs:
            self.id = str(uuid.uuid4())
            self.created_at = datetime.now()
            self.updated_at = self.created_at
            storage.new(self)
        else:
            f = "%Y-%m-%dT%H:%M:%S.%f"
            for key, value in kwargs.items():
                if key == 'created_at' or key == 'updated_at':
                    value = datetime.strptime(kwargs[key], f)
                if key != '__class__':
                    setattr(self, key, value)

    def to_dict(self):
        """ create new dictionary, adding a key and returning
        date or time to converted strings
        """
        new_dict = {}

    def __str__(self):
        """ return the class name, id and attribute dictionary """
        class_name = "[" + self.__class__.__name__ + "]"
        dct = {k: v for (k, v) in self.__dict__.items() if (not v) is False}
        return class_name + " (" + self.id + ") " + str(dct)

    def save(self):
        """ lateset update on time """
        self.updated_at = datetime.now()
        storage.save()


        for key, values in self.__dict__.items():
            if key == "created_at" or key == "updated_at":
                new_dict[key] = values.strftime("%Y-%m-%dT%H:%M:%S.%f")
            else:
                if not values:
                    pass
                else:
                    new_dict[key] = values
        new_dict['__class__'] = self.__class__.__name__

        return new_dict
